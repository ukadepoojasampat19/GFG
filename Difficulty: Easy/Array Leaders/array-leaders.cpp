class Solution {
    // Function to find the leaders in the array.
  public:
    vector<int> leaders(int n, int arr[]) {
        vector<int> result;
        int max_leader = arr[n-1];
        result.push_back(max_leader);
        
        for(int i=n-2;i>=0;i--)
        {
            if(arr[i] >= max_leader)
            {
                max_leader = arr[i];
                result.push_back(arr[i]);
            }
        }
        reverse(result.begin(),result.end());
        return result;
    }
};

